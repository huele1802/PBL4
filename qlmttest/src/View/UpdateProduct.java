    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        // TODO add your handling code here:
        try {
            if (txtmamay.getText().isEmpty() || txttenmay.getText().isEmpty() || txtxuatxu.getText().isEmpty()
                    || txtgia.getText().isEmpty() || txtDungLuongPin.getText().isEmpty() || txtkichthuoc.getText().isEmpty()
                    || txtloaimay.getText().isEmpty() || txtsoluong.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter all the required information!");
                return;
            }
            String maNhaCungCap = ((CBBItem) cbbmaNCC.getSelectedItem()).getValue();
            if (maNhaCungCap == null || maNhaCungCap.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please select a supplier!");
                return;
            }
            int soluong = Integer.parseInt(txtsoluong.getText());
            float gia = Float.parseFloat(txtgia.getText());
            float kichthuoc = Float.parseFloat(txtkichthuoc.getText());

            adminController.sendMessage("editProduct;" + txtmamay.getText() + "," + maNhaCungCap + ","
                    + txttenmay.getText() + "," + soluong + "," + gia + ","
                    + txtloaimay.getText() + "," + kichthuoc + "," + txtDungLuongPin.getText()
                    + "," + txtxuatxu.getText());
            this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddProductActionPerformed

