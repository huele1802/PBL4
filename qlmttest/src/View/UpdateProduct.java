    void setCombobox(List<CBBItem> list) {
        for (CBBItem item : list) {
            cbbmaNCC.addItem(item);
        }
    }

    CBBItem getSelectedCBBItem(String ma) {
        for (int i = 0; i < cbbmaNCC.getItemCount(); i++) {
            CBBItem item = (CBBItem) cbbmaNCC.getItemAt(i);
            if (item.getValue().equals(ma)) {
                return item;
            }
        }
        return null;
    }

    // Phương thức để set giá trị float vào JTextField
    private static void setFloatValue(JTextField textField, float value) {
        // Định dạng số để chuyển đổi giá trị float thành chuỗi
        DecimalFormat decimalFormat = new DecimalFormat("#.#####");
        textField.setText(decimalFormat.format(value));
    }

    private void displayInfo(MayTinh a) {
        txtmamay.setText(a.getmaMay());
        CBBItem item = getSelectedCBBItem(a.getmaNhaCungCap());
        if (item != null) {
            cbbmaNCC.setSelectedItem(item);
        }
        txtsoluong.setText(String.valueOf(a.getsoluong()));
        setFloatValue(txtgia, a.getgia());
        txttenmay.setText(a.gettenmay());
        txtxuatxu.setText(a.getxuatxu());
        txtloaimay.setText(a.getloaimay());
        txtkichthuoc.setText(String.valueOf(a.getkichthuoc()));
        txtDungLuongPin.setText(String.valueOf(a.getdungluongpin()));
    }

