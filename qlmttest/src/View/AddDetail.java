    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        if (tblSanPham.getSelectedRowCount() == 1) {
            int selected = tblSanPham.getSelectedRow();
            String mamay = tblSanPham.getValueAt(selected, 0).toString();
            String tenmay = tblSanPham.getValueAt(selected, 1).toString();
            float gia = Float.parseFloat(tblSanPham.getValueAt(selected, 2).toString());
            DefaultTableModel modelBill = (DefaultTableModel) tbHoaDon.getModel();
            boolean status = false;
            
            if (modelBill.getRowCount() > 0) {
                for (int i = 0; i < modelBill.getRowCount(); i++) {
                    if (modelBill.getValueAt(i, 0).toString().equals(mamay)) {
                        status = true;
                        int soluong = Integer.parseInt(modelBill.getValueAt(i, 2).toString());
                        modelBill.setValueAt(soluong + 1, i, 2);
                        break;
                    }
                }
            }
            if (!status) {
                modelBill.addRow(new Object[] {mamay, tenmay, 1, gia});
            }
            
            tbHoaDon.setModel(modelBill);
        }
        
        lbTotal.setText(totalBill());
    }//GEN-LAST:event_tblSanPhamMouseClicked

