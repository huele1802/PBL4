    public void loadTableProduct(DefaultTableModel model) {
        custom.CustomTable(tblSanPham);
        DefaultTableModel currentModel = (DefaultTableModel) tblSanPham.getModel();
        currentModel.setRowCount(0);

        // Load new data into the table
        for (int i = 0; i < model.getRowCount(); i++) {
            Object[] rowData = new Object[currentModel.getColumnCount()];
            rowData[0] = model.getValueAt(i, 0);
            rowData[1] = model.getValueAt(i, 2);
            rowData[2] = model.getValueAt(i, 4);
            currentModel.addRow(rowData);
        }
        custom.CustomTableAfterSetModel(tblSanPham, currentModel);
    }
    
