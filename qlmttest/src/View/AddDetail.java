    private String totalBill () {
        float total = 0;
        DefaultTableModel modelBill = (DefaultTableModel) tbHoaDon.getModel();
        if (modelBill.getRowCount() > 0) {
            for (int i = 0; i < modelBill.getRowCount(); i++) {
                int soluong = Integer.parseInt(modelBill.getValueAt(i, 2).toString());
                float gia = Float.parseFloat(modelBill.getValueAt(i, 3).toString());
                total += soluong * gia;
            }
        }
        
        String formattedValue = decimalFormat.format(total);
        return formattedValue;
    }

